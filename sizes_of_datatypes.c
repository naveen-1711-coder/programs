//print sizes of datatypes

#include<stdio.h>
#include<stdlib.h>

void main()
{
    printf("size of char: %d\n",sizeof(char));
    printf("size of int: %d\n",sizeof(int));
    printf("size of char *: %d\n",sizeof(char *));
    printf("size of int *: %d\n",sizeof(int *));
    printf("size of long int: %d\n",sizeof(long int));
    printf("size of long int *: %d\n",sizeof(long int *));
}